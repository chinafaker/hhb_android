public class MaintenanceInfoBean {

    private String mmDescription;

    private String mmEndtime;

    private String mmStarttime;

    private String mmSubtitle;

    private String mmTitle;

    public void setMmDescription(String mmDescription) {
        this.mmDescription = mmDescription;
    }

    public String getMmDescription() {
        return this.mmDescription;
    }

    public void setMmEndtime(String mmEndtime) {
        this.mmEndtime = mmEndtime;
    }

    public String getMmEndtime() {
        return this.mmEndtime;
    }

    public void setMmStarttime(String mmStarttime) {
        this.mmStarttime = mmStarttime;
    }

    public String getMmStarttime() {
        return this.mmStarttime;
    }

    public void setMmSubtitle(String mmSubtitle) {
        this.mmSubtitle = mmSubtitle;
    }

    public String getMmSubtitle() {
        return this.mmSubtitle;
    }

    public void setMmTitle(String mmTitle) {
        this.mmTitle = mmTitle;
    }

    public String getMmTitle() {
        return this.mmTitle;
    }
}
